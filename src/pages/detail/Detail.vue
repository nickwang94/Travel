<template>
  <div>
    <detail-banner :sightName="sightName" :bannerImg="bannerImg" :bannerImgs="gallaryImgs"></detail-banner>
    <detail-header></detail-header>
    <div class="content">
      <detail-list :list="list"></detail-list>
    </div>
  </div>
</template>

<script>
import DetailBanner from './components/Banner'
import DetailHeader from './components/Header'
import DetailList from './components/List'
import axios from 'axios'
export default {
  name: 'Detail', // 递归组件、keep-alive取消缓存、Vue组件显示的名字
  components: {DetailList, DetailBanner, DetailHeader},
  data () {
    return {
      sightName: '',
      bannerImg: '',
      gallaryImgs: [],
      list: []
    }
  },
  methods: {
    getDetailInfo () {
      axios.get('/api/detail.json', {
        params: {
          id: this.$route.params.id
        }
      }).then(this.handleGetDataSucc)
    },
    handleGetDataSucc (res) {
      res = res.data
      if (res.ret && res.data) {
        const data = res.data.dataDetail[0]
        this.sightName = data[0].sightName
        this.bannerImg = data[0].bannerImg
        this.gallaryImgs = data[0].gallaryImgs
        this.list = data[0].categoryList
      }
    }
  },
  mounted () {
    this.getDetailInfo()
  }
}
</script>

